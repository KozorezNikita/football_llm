import type { SquadReport, SlotAnalysis } from "../data/squadAnalysis";

// Будує промпт для ШІ-звіту по складу. Математика вже все порахувала —
// модель ЛИШЕ озвучує: переказує + пов'язує зони + додає футбольний контекст.
// Жорстко заборонено вигадувати факти поза наданими числами.

function slotLine(s: SlotAnalysis): string {
  const side = s.side === "L" ? " (лівий)" : s.side === "R" ? " (правий)" : "";
  const starter = s.starter
    ? `${s.starter.name} ${s.starterRating?.toFixed(2)}`
    : "немає гравця";
  const pr = s.priority === "high" ? "ВИСОКИЙ" : s.priority === "medium" ? "середній" : "низький";
  return `- ${s.label}${side}: ${starter}, глибина ${s.depth}, пріоритет підсилення ${pr}`;
}

export function buildSquadReportContext(report: SquadReport, teamName: string): string {
  const lines: string[] = [];
  lines.push(`Команда: ${teamName}`);
  lines.push(`Основна формація (визначена за сумарним рейтингом складу): ${report.formation}`);
  lines.push("");
  lines.push("Аналіз по позиціях (стартер, його середній рейтинг за сезон, глибина = кількість адекватних замін, пріоритет підсилення):");
  for (const s of report.slots) lines.push(slotLine(s));
  return lines.join("\n");
}

